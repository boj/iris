//! iris-self-compile: Run c_backend.json on interpreter.json to generate C.
//!
//! Usage:
//!   iris-self-compile [c_backend.json] [interpreter.json]

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let backend_path = args.get(1).map(|s| s.as_str()).unwrap_or("bootstrap/c_backend.json");
    let target_path = args.get(2).map(|s| s.as_str()).unwrap_or("bootstrap/interpreter.json");

    let backend = iris_bootstrap::load_graph(backend_path)
        .unwrap_or_else(|e| { eprintln!("error: {}: {}", backend_path, e); std::process::exit(1); });

    let target = iris_bootstrap::load_graph(target_path)
        .unwrap_or_else(|e| { eprintln!("error: {}: {}", target_path, e); std::process::exit(1); });

    let target_val = iris_types::eval::Value::Program(std::rc::Rc::new(target.clone()));
    let root_id = target.root.0 as i64;

    // Call c_backend with (target_graph, root_node_id, ctx=1)
    let result = iris_bootstrap::evaluate(&backend, &[
        target_val,
        iris_types::eval::Value::Int(root_id),
        iris_types::eval::Value::Int(1),  // statement context
    ]).unwrap_or_else(|e| { eprintln!("error: {:?}", e); std::process::exit(1); });

    // Wrap with C header/footer
    print!("/* Generated by c_backend.iris — IRIS compiles itself */\n");
    print!("#include \"iris_rt.h\"\n\n");
    print!("iris_value_t *iris_interpret(iris_value_t *program, iris_value_t *inputs) {{\n");
    match &result {
        iris_types::eval::Value::String(s) => print!("{}", s),
        other => { eprintln!("error: expected String, got {:?}", other); std::process::exit(1); }
    }
    print!("\n}}\n");
}
